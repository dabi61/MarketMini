package model;

/**
 *
 * @author Admin
 */
public  class RoleItem {
    private String name;
    private int value;

    public RoleItem(String name, int value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public int getValue() {
        return value;
    }

    @Override
    public String toString() {
        return name; // Hiển thị tên trong JComboBox
    }
}
